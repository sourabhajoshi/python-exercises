"""
Find the length of the text python and convert the value to float and convert it to string
"""

text = "Python"

print("Length of text is : ", len(text), "Type is : ", type(len(text)))
print("Float of length is : ", float(len(text)), "Type is is", type(float(len(text))))
print("String of length is : ", str(float(len(text))), "type is :", type(str(float(len(text)))))